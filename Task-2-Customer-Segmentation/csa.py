#KEY PERFORMANCE INDICATORS
data['Totalpurchases']=data['NumWebPurchases']+data['NumCatalogPurchases']+data['NumStorePurchases']+data['NumCatalogPurchases']

avg_purchase_value=data['MntTotal'].sum()/data['Totalpurchases'].sum()
