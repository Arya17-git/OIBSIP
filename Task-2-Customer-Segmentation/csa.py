"""**CUSTOMER SEGMENTATION**"""

#FEATURE SELECTION & SCALING
features = ['Income', 'MntTotal', 'Recency', 'NumWebPurchases', 'NumStorePurchases']
data_to_cluster = data[features]

scaler = StandardScaler()
scaled_features = scaler.fit_transform(data_to_cluster)

#ELBOW METHOD
wcss = [] # Within-Cluster Sum of Square
for i in range(1, 11):
    kmeans = KMeans(n_clusters=i, init='k-means++', random_state=42)
    kmeans.fit(scaled_features)
    wcss.append(kmeans.inertia_)

# Plotting the Elbow
plt.figure(figsize=(8, 5))
plt.plot(range(1, 11), wcss, marker='o', linestyle='--')
plt.title('Elbow Method to Determine Optimal Clusters')
plt.xlabel('Number of Clusters')
plt.ylabel('WCSS')
plt.show()

#K-MEANS CLUSTERING
kmeans = KMeans(n_clusters=4, init='k-means++', random_state=42)
clusters = kmeans.fit_predict(scaled_features)

data['Cluster'] = clusters

cluster_summary = data.groupby('Cluster')[features].mean()
print(cluster_summary)

# Calculate the mean of each key feature for every cluster
profile = data.groupby('Cluster')[['Income', 'MntTotal', 'Recency', 'NumWebPurchases']].mean()
print(profile)

