"""**MERGING ALL THE FILES TOGETHER**"""

import pandas as pd
import os
import glob

# 1. Get a list of all CSV files in that folder
all_files = glob.glob(os.path.join(path, "*.csv"))

df_list = []
for file in all_files:
    temp_df = pd.read_csv(file, encoding='latin-1')

    temp_df['region'] = os.path.basename(file)[:2]

    df_list.append(temp_df)

df = pd.concat(df_list, ignore_index=True)

print(f"Total rows loaded: {len(df)}")

"""**HANDLING MISSING VALUES**"""

df.isnull().sum().sum()

df.isnull().sum()

df['description']=df['description'].fillna('NO DESCRIPTION!!')

df.isnull().sum().sum()

"""**INTEGRITY CHECK**"""

import pandas as pd

# Convert 'publish_time' and 'trending_date' to datetime objects
df['publish_time'] = pd.to_datetime(df['publish_time'])
df['trending_date'] = pd.to_datetime(df['trending_date'], format='%y.%d.%m')

# A video cannot be trending before it was published
time_error = df[df['publish_time'].dt.date > df['trending_date'].dt.date]
print(f"{len(time_error)} videos trended before they were published")

# Check data types of all columns
print(df.dtypes)
#to find if any numeric value contained some non-numeric values
non_numeric_likes = pd.to_numeric(df['likes'], errors='coerce').isna().sum()
print(f"Non-numeric values in 'likes': {non_numeric_likes}")

# List unique values in the boolean columns
print("Values in comments_disabled:", df['comments_disabled'].unique())
print("Values in ratings_disabled:", df['ratings_disabled'].unique())

"""**REMOVING DUPLICATES**"""

# Remove rows where every single column is same
df = df.drop_duplicates()
print(f"Rows after removing exact duplicates: {len(df)}")

# Count how many times each video ID appears
duplicate_counts = df['video_id'].value_counts()
print(df['video_id'].duplicated().sum(), "duplicate entries found.")

# 1. Sort by views (highest to lowest)
df = df.sort_values('views', ascending=False)

# 2. Keep only the first (highest view) entry for each unique video
df_unique_videos = df.drop_duplicates(subset='video_id', keep='first')

print(f"Unique videos found: {len(df_unique_videos)}")

total_rows = len(df)

unique_count = df['video_id'].nunique()

extra_entries = df.duplicated(subset='video_id').sum()
