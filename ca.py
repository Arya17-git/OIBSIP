print(f"Actual Total Rows: {total_rows}")

"""**STANDARDIZATION**


*   Already standardized the "publish_time" and "trending_date" colmuns during integrity checking part.

"""

# Create standardized engagement ratios
df['likes_per_view'] = df['likes'] / df['views']
df['comments_per_view'] = df['comment_count'] / df['views']
print(df[['likes_per_view', 'comments_per_view']].head())

"""**OUTLIERS**"""

# Calculate IQR for the 'views' column
Q1 = df['views'].quantile(0.25)
Q3 = df['views'].quantile(0.75)
IQR = Q3 - Q1

# Defining the boundary
lower_bound = Q1 - 1.5 * IQR
upper_bound = Q3 + 1.5 * IQR

# Find the outliers
outliers = df[(df['views'] < lower_bound) | (df['views'] > upper_bound)]

print(f"Typical View Range: {lower_bound} to {upper_bound}")
print(f"Total Outliers detected: {len(outliers)}")

import matplotlib.pyplot as plt
import seaborn as sns

plt.figure(figsize=(10, 5))
sns.boxplot(x=df['views'])
plt.title('Detection of View Outliers')
plt.show()

# REMOVAL OF OUTLIERS
df_trimmed = df[df['views'] <= upper_bound].copy()

print(f"Original rows: {len(df)}")
print(f"Rows after trimming: {len(df_trimmed)}")
print(f"Max views in trimmed data: {df_trimmed['views'].max()}")

#SCALING
import numpy as np
df['views_log'] = np.log1p(df['views'])

# Visualize the difference
import seaborn as sns
